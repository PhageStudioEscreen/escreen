const struct keycap_color * keycap_color_random(void)
{
    return &keycap_colors[(rand() % (KEYCAP_COLOR_MAX - 1)) + 1];
}

const struct keycap_color * keycap_color_random_with_coloration(const char * coloration)
{
    uint8_t indexs[KEYCAP_COLOR_MAX];
    uint8_t length = 0;

    /* do filter */
    for(uint8_t i = 0; i < KEYCAP_COLOR_MAX; i++) {
        if(!strncmp(keycap_colors[i].name, coloration, strlen(coloration))) {
            indexs[length] = i;
            length++;
        }
    }

    if(!length) {
        return &keycap_colors[0];
    }

    return &keycap_colors[indexs[rand() % length]];
}

const struct keycap_color * keycap_color_get_by_index(uint32_t index)
{
    if(index >= KEYCAP_COLOR_MAX) {
        return &keycap_colors[0];
    }
    return &keycap_colors[index];
}
